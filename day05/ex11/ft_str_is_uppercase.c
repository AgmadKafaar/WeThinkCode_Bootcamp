#include <stdio.h>
#include <stdlib.h>

int		ft_str_is_uppercase(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if (!(str[i] >= 'A' && str[i] <= 'Z'))
			return (0);
		i++;
	}
	return (1);
}

int		main(void)
{
	char example[] = "DFAJGJA45";
	printf("%i\n", ft_str_is_uppercase(example));
	return (0);
}
