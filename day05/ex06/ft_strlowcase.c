#include <stdio.h>
#include <stdlib.h>

char	*ft_strlowcase(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
			str[i] = str[i] + ('a' - 'A');
		i++;
	}
	return (str);
}

int		main(void)
{
	char example[] = "HELLo JOhn 3829";
	printf("Lower case = %s\n", ft_strlowcase(example));
	return (0);
}
